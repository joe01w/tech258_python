# Operators

x = 5
y = 1

# arithmetic operators
result = x + y + 2
print("Let's show x + y + 2 =", result)
result = x - y - 2
print("Let's show x - y - 2 =", result)
result = x * y * 2
print("Let's show x * y * 2 =", result)
result = x / y / 2
print("Let's show x / y / 2 =", result)
# % modulo --> Remainder of division
result = x % y % 2
print("Let's show x % y % 2 =", result)

print(5 % 4) #1 left over

# Comparison Operators - >, <, ==, !=, >=, <=
result = x > y # should be True
print("Let's show x > y =", result)

result = x < y # should be False
print("Let's show x < y =", result)

result = x == y # should be False
print("Let's show x == y =", result)

result = x != y # should be True
print("Let's show x != y =", result)

result = x >= y # should be True
print("Let's show x >= y =", result)

result = x <= y # should be False
print("Let's show x <= y =", result)